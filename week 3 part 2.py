x,y =input ("Enter 2 decimal numbers: ").split()
print ("First number: ", x)
print ("Second number: ", y)
addition= float(x) + float(y)

subtraction= float(x) - float(y)

multiplication= float(x) * float(y)

division= float(x) / float(y)

print ("the numbers added = %.2f " % (addition))
print ("the numbers subtracted = %.2f " % (subtraction))
print ("the numbers multiplied = %.2f "% (multiplication))
print ("the numbers divided = %.2f "% (division))